unsigned char cursor_rgba[];

enum program_type {
   PROGRAM_RGB,
   PROGRAM_RGBA,
   PROGRAM_LAST,
};

enum {
   UNIFORM_WIDTH,
   UNIFORM_HEIGHT,
   UNIFORM_DIM,
   UNIFORM_LAST,
};

static const char *uniform_names[UNIFORM_LAST] = {
   "width",
   "height",
   "dim"
};

struct gl_context {
   struct {
      GLuint obj;
      GLuint uniforms[UNIFORM_LAST];
   } programs[PROGRAM_LAST];

   GLuint cursor;
   enum program_type current_program;
   uint32_t width, height;
};

